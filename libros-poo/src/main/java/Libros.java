public class Libros {
    
    //atributos
    private int ISBN;
    private String titulo;
    private String autor;
    private short numPaginas;
    
    //Metodo constructor
    public Libros(int ISBN, String titulo, String autor, short numPaginas){
        this.ISBN = ISBN;
        this.titulo = titulo;
        this.autor = autor;
        this.numPaginas = numPaginas;
        
    }
    

}
